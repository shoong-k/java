public class Ex2_5 {
    public static void main(String[] args) {
        int x = 5;
        int y = 3;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

        x = 10;
        y = 5;

        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
    }
}
